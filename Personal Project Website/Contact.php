      <script type="text/javascript">
          function sendEmail(){
            var name=$("#name");
            var email=$("#email");
            var phone=$("#phone");
            var mssg=$("#mssg");

            if(isNotEmpty(name) && isNotEmpty(email) && isNotEmpty(phone) && isNotEmpty(mssg)){
              $.ajax({
                url: 'sendEmail.php',
                method: 'POST',
                dataType: 'json',
                data: {
                  name: name.val(),
                  email:email.val(),
                  phone: phone.val(),
                  mssg: mssg.val()
                }, success: function(response){
                  $('#myForm')[0].reset();
                  $('.sent-notification').text("Message sent Successfully...");
                }
              })
            }
          }
          function isNotEmpty(caller)
          {
            if(caller.val()==""){
              caller.css('border','1px solid red');
              return false;
            }
            else{
              caller.css('border','');
              return true;
            }
          }
